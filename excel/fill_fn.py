def get_col_index(ws, col_name):
    """헤더 행에서 컬럼명의 인덱스 반환 (1-based)"""
    for cell in ws[1]:
        if cell.value and col_name in str(cell.value):
            return cell.column
    return None


def fill_fn_sheet():
    excel_path = "/Users/simjaehyeong/Desktop/pando/pando-beta/excel/스포짐_기획문서_v2.xlsx"

    print(f"엑셀 파일 열기: {excel_path}")
    wb = openpyxl.load_workbook(excel_path)

    sheet_name = "기능명세서"
    if sheet_name not in wb.sheetnames:
        print(f"오류: '{sheet_name}' 시트를 찾을 수 없습니다.")
        print(f"사용 가능한 시트: {wb.sheetnames}")
        return

    ws = wb[sheet_name]
    print(f"시트 '{sheet_name}' 열림. 총 행 수: {ws.max_row}, 컬럼 수: {ws.max_column}")

    # 헤더 행 확인
    print("\n헤더 행 내용:")
    for cell in ws[1]:
        if cell.value:
            print(f"  Col {cell.column} ({cell.column_letter}): {cell.value}")

    # 컬럼 인덱스 찾기
    fn_id_col = None
    biz_rule_col = None
    exception_col = None
    web_impl_col = None

    for cell in ws[1]:
        val = str(cell.value) if cell.value else ""
        if "기능 ID" in val or "기능ID" in val:
            fn_id_col = cell.column
        elif "비즈니스 규칙" in val:
            biz_rule_col = cell.column
        elif "예외 처리" in val:
            exception_col = cell.column
        elif "Web 구현" in val:
            web_impl_col = cell.column

    print(f"\n컬럼 인덱스:")
    print(f"  기능 ID: {fn_id_col}")
    print(f"  비즈니스 규칙: {biz_rule_col}")
    print(f"  예외 처리: {exception_col}")
    print(f"  Web 구현 사항: {web_impl_col}")

    if not all([fn_id_col, biz_rule_col, exception_col, web_impl_col]):
        # 컬럼을 못 찾으면 고정 인덱스 사용 (K=11, L=12, M=13)
        print("\n컬럼 자동 탐지 실패. 고정 인덱스 사용 (K=11, L=12, M=13)")
        if not fn_id_col:
            fn_id_col = 1  # A열
        if not biz_rule_col:
            biz_rule_col = 11  # K열
        if not exception_col:
            exception_col = 12  # L열
        if not web_impl_col:
            web_impl_col = 13  # M열

    # wrap text 정렬 설정
    wrap_align = Alignment(wrap_text=True, vertical="top")

    updated_count = 0
    skipped_count = 0

    for row in ws.iter_rows(min_row=2):
        fn_id_cell = row[fn_id_col - 1]
        fn_id = str(fn_id_cell.value).strip() if fn_id_cell.value else ""

        # 그룹 헤더 행 (▼ SCR-XXX) 건너뜀
        if fn_id.startswith("▼") or not fn_id.startswith("FN-"):
            continue

        if fn_id not in FN_DATA:
            continue

        data = FN_DATA[fn_id]

        biz_cell = row[biz_rule_col - 1]
        exc_cell = row[exception_col - 1]
        web_cell = row[web_impl_col - 1]

        changed = False

        # 비즈니스 규칙 (빈 셀만 채움)
        if not biz_cell.value or str(biz_cell.value).strip() == "":
            biz_cell.value = data["비즈니스_규칙"]
            biz_cell.alignment = wrap_align
            changed = True
            print(f"  [{fn_id}] 비즈니스 규칙 채움")
        else:
            print(f"  [{fn_id}] 비즈니스 규칙 이미 있음 - 건너뜀")

        # 예외 처리 (빈 셀만 채움)
        if not exc_cell.value or str(exc_cell.value).strip() == "":
            exc_cell.value = data["예외_처리"]
            exc_cell.alignment = wrap_align
            changed = True
            print(f"  [{fn_id}] 예외 처리 채움")
        else:
            print(f"  [{fn_id}] 예외 처리 이미 있음 - 건너뜀")

        # Web 구현 사항 (빈 셀만 채움)
        if not web_cell.value or str(web_cell.value).strip() == "":
            web_cell.value = data["Web_구현"]
            web_cell.alignment = wrap_align
            changed = True
            print(f"  [{fn_id}] Web 구현 사항 채움")
        else:
            print(f"  [{fn_id}] Web 구현 사항 이미 있음 - 건너뜀")

        if changed:
            updated_count += 1
        else:
            skipped_count += 1

    print(f"\n저장 중...")
    wb.save(excel_path)
    print(f"저장 완료: {excel_path}")
    print(f"\n결과: {updated_count}개 행 업데이트, {skipped_count}개 행 건너뜀")


if __name__ == "__main__":
    fill_fn_sheet()
