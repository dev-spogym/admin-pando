def main():
    wb = load_workbook(EXCEL_PATH)
    ws = wb[SHEET_NAME]

    updated = 0
    skipped = 0

    for row in range(1, ws.max_row + 1):
        fn_id = ws.cell(row, 1).value
        if not fn_id or not str(fn_id).startswith("FN-"):
            skipped += 1
            continue

        fn_key = str(fn_id).strip()
        if fn_key not in FN_DATA:
            print(f"  [경고] {fn_key} 데이터 없음 – 건너뜀")
            skipped += 1
            continue

        biz_rule, exception, web_impl = FN_DATA[fn_key]
        fn_name = ws.cell(row, 2).value

        ws.cell(row, 11).value = biz_rule   # 비즈니스 규칙 (col K)
        ws.cell(row, 12).value = exception  # 예외 처리 (col L)
        ws.cell(row, 13).value = web_impl   # Web 구현 사항 (col M)

        print(f"  [OK] 행{row}: {fn_key} ({fn_name}) → K/L/M 덮어쓰기 완료")
        updated += 1

    wb.save(EXCEL_PATH)
    print(f"\n완료: {updated}개 기능 업데이트, {skipped}개 행 건너뜀")
    print(f"저장 경로: {EXCEL_PATH}")


if __name__ == "__main__":
    main()
