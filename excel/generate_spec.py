def write_sheet(ws, headers, widths, data, center_cols=set(), status_col=None, priority_col=None):
    ws.freeze_panes(1,0)
    for i,(h,w) in enumerate(zip(headers,widths)):
        ws.set_column(i,i,w)
        ws.write(0,i,h,F['h'])
    for r,row in enumerate(data,1):
        for c,v in enumerate(row):
            if priority_col is not None and c==priority_col:
                fm = F.get({'Critical':'crit','High':'high','Medium':'med'}.get(v,'cc'), F['cc'])
                ws.write(r,c,v,fm)
            elif status_col is not None and c==status_col:
                fm = F['ok'] if v=='확정' else F['wip'] if v in ('작성중','개발중') else F['cc']
                ws.write(r,c,v,fm)
            elif c in center_cols:
                ws.write(r,c,v,F['cc'])
            else:
                ws.write(r,c,v,F['c'])

