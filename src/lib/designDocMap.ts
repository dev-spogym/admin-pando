/**
 * 현재 경로에 해당하는 설계서 문서를 찾는 함수.
 * 정확한 매칭 -> 동적 라우트 패턴 매칭 순으로 시도.
 */
export function getDesignDoc(pathname: string): DesignDoc | null {
  // 정확한 매칭
  if (DESIGN_DOC_MAP[pathname]) {
    return DESIGN_DOC_MAP[pathname];
  }

  // 동적 라우트 처리 (예: /members/detail?id=123 -> /members/detail)
  const basePath = pathname.split('?')[0];
  if (DESIGN_DOC_MAP[basePath]) {
    return DESIGN_DOC_MAP[basePath];
  }

  // 경로 끝 슬래시 제거
  const trimmed = basePath.endsWith('/') ? basePath.slice(0, -1) : basePath;
  if (DESIGN_DOC_MAP[trimmed]) {
    return DESIGN_DOC_MAP[trimmed];
  }

  return null;
}
